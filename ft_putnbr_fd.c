#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	neg;

	neg = 1;
	if (n < 0)
	{
		neg = -1;
		write(fd, "-", 1);
	}
	if (n == 0)
		write(fd, "0", 1);
	if (n != 0)
	{
		if (n / 10 != 0)
			ft_putnbr_fd(n / 10 * neg, fd);
		write(fd, &(char){n % 10 * neg + '0'}, 1);
	}
}
