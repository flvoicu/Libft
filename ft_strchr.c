#include "stdlib.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*a;

	i = 0;
	a = (char *)s;
	while (c >= 256)
		c -= 256;
	while (s[i] != '\0')
	{
		if (*(s + i) == c)
			return ((a + i));
		i++;
	}
	if (*(s + i) == c)
		return ((a + i));
	return (NULL);
}
