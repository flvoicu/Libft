#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*a;
	char	*tmp;

	a = malloc((ft_strlen(s1) + ft_strlen(s2) + 1) * sizeof(char));
	if (a == NULL)
		return (NULL);
	tmp = a;
	while (*s1)
	{
		*tmp = *s1;
		tmp++;
		s1++;
	}
	while (*s2)
	{
		*tmp = *s2;
		tmp++;
		s2++;
	}
	*tmp = '\0';
	return (a);
}
