#include "stdlib.h"

int	ft_atoi(const char *str)
{
	int		neg;
	int		val;
	char	*s;

	neg = 1;
	val = 0;
	s = (char *)str;
	while (*s == ' ' || (*str >= 9 && *s <= 13))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			neg = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		val = val * 10 + *s - '0';
		s++;
	}
	val *= neg;
	return (val);
}
